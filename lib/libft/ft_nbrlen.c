#include "libft.h"

int		ft_nbrlen(long nbr)
{
	int	i;

	i = 1;
	if (nbr < 0)
	{
		nbr = -nbr;
		i++;
	}
	while ((nbr = nbr / 10))
		i++;
	return (i);
}
