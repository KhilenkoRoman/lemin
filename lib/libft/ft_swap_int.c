void	ft_swap_int(int *x, int *y)
{
	int x2;
	int y2;

	x2 = (*y);
	y2 = (*x);
	(*x) = x2;
	(*y) = y2;
}

void	ft_swap_dbl(double *x, double *y)
{
	double x2;
	double y2;

	x2 = (*y);
	y2 = (*x);
	(*x) = x2;
	(*y) = y2;
}
