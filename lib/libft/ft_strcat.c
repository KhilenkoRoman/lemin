#include <string.h>

char	*ft_strcat(char *s1, const char *s2)
{
	size_t len;
	size_t i;

	len = 0;
	i = 0;
	while (s1[len] != '\0')
	{
		len++;
	}
	while (s2[i] != '\0')
	{
		s1[len++] = s2[i++];
	}
	s1[len] = '\0';
	return (s1);
}
