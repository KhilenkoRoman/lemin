#include "libft.h"

void	ft_putstr(char const *s)
{
	int		i;
	char	*str;

	str = (char*)s;
	i = 0;
	if (s != NULL)
	{
		while (str[i] != '\0')
			ft_putchar(s[i++]);
	}
}
